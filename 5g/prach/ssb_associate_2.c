/* 38.213 Section 8.1 Random access preamble */
int main(int argc, char *argv[])
{
    int FR = 0;
    int Lmax = 4;
    int configIndex = 98;
    int msg1FDM = 1;
    char *ssb_perRACH_Occasion = "1/8";
    int N = -8;

    int ch;


    opterr = 0;
    while ((ch=getopt(argc, argv, "f:l:c:m:n:h")) != -1)
    {
        switch ( ch )
        {
            case 'f':
                FR = atoi( optarg );
                if (FR > 2) FR = 0;
                break;
            case 'l':
                Lmax = atoi( optarg );
                if ((Lmax != 4) && (Lmax != 8) && (Lmax != 64))
                {
                    printf("ERR: wrong L_max %s\n\n", optarg);
                    return 0;
                }
                break;
            case 'c':
                configIndex = (atoi( optarg ) & 0xFF);
                break;
            case 'm':
                msg1FDM = atoi( optarg );
                if ((msg1FDM != 1) &&
                    (msg1FDM != 2) &&
                    (msg1FDM != 4) &&
                    (msg1FDM != 8))
                {
                    printf("ERR: wrong msg1-FDM %s\n\n", optarg);
                    return 0;
                }
                break;
            case 'n':
                ssb_perRACH_Occasion = optarg;
                break;
            case 'h':
            default:
                help();
                return 0;
        }
    }


    if (0 == strcmp("1/8", ssb_perRACH_Occasion))
    {
        N = -8;
    }
    else if (0 == strcmp("1/4", ssb_perRACH_Occasion))
    {
        N = -4;
    }
    else if (0 == strcmp("1/2", ssb_perRACH_Occasion))
    {
        N = -2;
    }
    else if (0 == strcmp("1", ssb_perRACH_Occasion))
    {
        N = 1;
    }
    else if (0 == strcmp("2", ssb_perRACH_Occasion))
    {
        N = 2;
    }
    else if (0 == strcmp("4", ssb_perRACH_Occasion))
    {
        N = 4;
    }
    else if (0 == strcmp("8", ssb_perRACH_Occasion))
    {
        N = 8;
    }
    else if (0 == strcmp("16", ssb_perRACH_Occasion))
    {
        N = 16;
    }
    else
    {
        printf("ERR: wrong ssb-perRACH-Occasion %s\n\n", ssb_perRACH_Occasion);
        return 0;
    }


    printf("FR = ");
    if (0 == FR)
    {
        printf("FR1 and paired spectrum\n");
        if ((Lmax != 4) && (Lmax != 8))
        {
            printf("ERR: wrong L_max %d\n\n", Lmax);
            return 0;
        }
    }
    else if (1 == FR)
    {
        printf("FR1 and unpaired spectrum\n");
        if ((Lmax != 4) && (Lmax != 8))
        {
            printf("ERR: wrong L_max %d\n\n", Lmax);
            return 0;
        }
    }
    else
    {
        printf("FR2 and unpaired spectrum\n");
        if (Lmax != 64)
        {
            printf("ERR: wrong L_max %d\n\n", Lmax);
            return 0;
        }
    }
    printf("PRACH configuration index = %d\n", configIndex);
    printf("ssb-perRACH-Occasion = %s\n", ssb_perRACH_Occasion);
    printf("msg1-FDM = %d\n", msg1FDM);
    printf("Lmax = %d\n", Lmax);
    printf("\n");


    if (FR < 2)
    {
        ssbPrachAssocFR1(
            FR,
            Lmax,
            configIndex,
            msg1FDM,
            N
        );
    }
    else
    {
        ssbPrachAssocFR2(
            FR,
            Lmax,
            configIndex,
            msg1FDM,
            N
        );
    }


    return 0;
}


